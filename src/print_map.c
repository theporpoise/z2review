#include "bisquick.h"
#include <unistd.h>

void	print_map(char **map)
{
	int		i;

	i = 1;
	if (map[1][0] == '\0')
	{
		write(2, "map error\n", 10);
	}
	while (**(map + i))
	{
		my_putstr(*(map + i));
		i++;
	}
	return ;
}
